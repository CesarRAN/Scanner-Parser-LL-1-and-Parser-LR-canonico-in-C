void popLR()
{
	t_StackLR *aux;

	if (ptrStackLRTop != NULL) //Hay elementos en el Stack
	{
		aux = ptrStackLRTop;
		ptrStackLRTop = ptrStackLRTop->ptrSig;
		free(aux);
	}//if
}//popLR

void pops_LR(int intNumPops)
{
	for (int i = 0; i < intNumPops; i++)
	{
		popLR();
	}
}

void vaciar_StackLR()
{
	while (ptrStackLRTop != NULL)
		popLR();

}//vaciar_StackLR

